package com.example.dao;
public class Person{
	
	private int uid;
	private String name;
	private int age;
	
	public int getUid(){
		return this.uid;
	}
	
	public void setUid(int uid){
		this.uid = uid;
	}
	
	public String getName(){
		return this.name;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public int getAge(){
		return this.age;
	}
	
	public void setAge(int age){
		this.age = age;
	}
	
}